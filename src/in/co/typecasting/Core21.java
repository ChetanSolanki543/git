package in.co.typecasting;

// Type Casting 

public class Core21 {

	public static void main(String[] args) {
		
		String S1 = "100";
		
		int i1 = Integer.parseInt(S1);		// convert String into integer
		
		String S2 = "200";
		
		int i2 = Integer.parseInt(S2);
		
		System.out.println(S1+S2);
		
		System.out.println(i1+i2);

	}

}
